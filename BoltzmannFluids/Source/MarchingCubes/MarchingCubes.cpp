std::unique_ptr<Mesh> MarchingCubes::compute(Texture3D& source_texture, int32_t mipmap)
{
	if (mipmap != 0) {
		std::cout << "[MarchingCubes Error] MarchingCubes::compute() is called but mipmap != 0 is not implemented yet" << std::endl;
		ASSERT(false);
	}

	size_t vertex_buffer_size = get_vertex_buffer_needed_size_in_bytes(source_texture.get_size());
	size_t index_buffer_size = get_index_buffer_needed_size_in_bytes(source_texture.get_size());

	auto mesh = std::make_unique<Mesh>();

	std::shared_ptr<VertexAttributeBuffer> vab = std::make_shared<VertexAttributeBuffer>();
	std::shared_ptr<Buffer> position_buffer = std::make_shared<Buffer>(vertex_buffer_size);
	std::shared_ptr<Buffer> index_buffer = std::make_shared<Buffer>(index_buffer_size);

	compute(source_texture, mipmap, *position_buffer, *index_buffer);

	vab->attach_vertex_buffer(Mesh::vab_vertex_slot, position_buffer, sizeof(glm::vec3), 0);
	vab->set_attribute_format(Mesh::vab_vertex_slot, Mesh::vab_vertex_slot, VertexAttributeBuffer::a_f32, 3, 0, true);

	mesh->set_vertex_attribute_buffer(vab);
	mesh->set_index_buffer(index_buffer);

	return mesh;
}

void MarchingCubes::compute(Texture3D& source_texture, int32_t mipmap, Buffer& out_vertex, Buffer& out_index)
{
	if (mipmap != 0) {
		std::cout << "[MarchingCubes Error] MarchingCubes::compute() is called but mipmap != 0 is not implemented yet" << std::endl;
		ASSERT(false);
	}

	_compile_shaders(source_texture.get_internal_format_color());
	ComputeProgram& kernel = *marching_cubes;

	kernel.update_uniform_as_image("volume", source_texture, mipmap);
	kernel.update_uniform_as_storage_buffer("vertex_buffer", out_vertex, 0);
	kernel.update_uniform_as_storage_buffer("index_buffer", out_index, 0);
    kernel.update_uniform_as_uniform_buffer("indicies_look_up_table", *indicies_look_up_table, 0);

	kernel.update_uniform("volume_resolution", source_texture.get_size().x, source_texture.get_size().y, source_texture.get_size().z);
	kernel.update_uniform("threshold", 1.0f);
	kernel.update_uniform("mesh_scale", glm::vec3(1));
}

