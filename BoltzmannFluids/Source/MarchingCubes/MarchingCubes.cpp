size_t MarchingCubes::get_index_buffer_needed_size_in_bytes(glm::ivec3 volume_resolution)
{
	size_t voxel_count = volume_resolution.z * volume_resolution.y * volume_resolution.x;
	constexpr int32_t max_indicies_per_voxel = 15;
	return voxel_count * max_indicies_per_voxel;
}

size_t MarchingCubes::get_vertex_buffer_needed_size_in_bytes(glm::ivec3 volume_resolution)
{
	size_t vertex_count = (volume_resolution.z + 1) * (volume_resolution.y + 1) * (volume_resolution.x + 1);
	int32_t vertex_size_in_bytes = sizeof(glm::vec3);
	return vertex_count * vertex_size_in_bytes;
}

