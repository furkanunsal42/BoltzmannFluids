void MarchingCubes::_compile_shaders(Texture3D::ColorTextureFormat volume_internal_format)
{
	if (compiled && compiled_format == volume_internal_format)
		return;
	
	std::vector<std::pair<std::string, std::string>> macro_definitions = {
		{"volume_internal_format", Texture3D::ColorTextureFormat_to_OpenGL_compute_Image_format(volume_internal_format)},
		{"volume_type", Texture3D::ColorTextureFormat_to_OpenGL_compute_Image_type<Texture3D>(volume_internal_format)},
	};

	auto macro_definitions_combined = macro_definitions;
	macro_definitions_combined.push_back({ "compute_verticies", "1" });
	macro_definitions_combined.push_back({ "compute_indicies", "1" });

	auto macro_definitions_vertex = macro_definitions;
	macro_definitions_vertex.push_back({ "compute_verticies", "1" });
	macro_definitions_vertex.push_back({ "compute_indicies", "0" });

	auto macro_definitions_index = macro_definitions;
	macro_definitions_index.push_back({ "compute_verticies", "0" });
	macro_definitions_index.push_back({ "compute_indicies", "1" });

	marching_cubes = std::make_shared<ComputeProgram>(Shader(marching_cubes_shader_directory / "marching_cubes.comp"), macro_definitions_combined);
	marching_cubes_vertex_only = std::make_shared<ComputeProgram>(Shader(marching_cubes_shader_directory / "marching_cubes.comp"), macro_definitions_vertex);
	marching_cubes_index_only = std::make_shared<ComputeProgram>(Shader(marching_cubes_shader_directory / "marching_cubes.comp"), macro_definitions_index);

	indicies_look_up_table = std::make_shared<UniformBuffer>();
	indicies_look_up_table->push_variable_array(256 * 5);
    
    for (int32_t case_code = 0; case_code < 256; case_code++) {
        for (int32_t triangle = 0; triangle < 5; triangle++) {
            
            glm::ivec4 ivec4_trig(
                edge_connect_list[case_code][triangle*3 + 0],
                edge_connect_list[case_code][triangle*3 + 1],
                edge_connect_list[case_code][triangle*3 + 2],
                0
            );
            
            indicies_look_up_table->set_data(case_code*5 + triangle, 0, sizeof(glm::ivec4), &ivec4_trig);
        }
    }

	compiled = true;
	compiled_format = volume_internal_format;
}
